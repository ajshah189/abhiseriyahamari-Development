/**
 * Backwards-compat alias used by existing importers (passengerService,
 * guestDatabaseService, MapScreen, DirectoryPage, AdminPage).
 * Maps new ROOMS fields to the shape the rest of the app expects:
 *   name    → room.number  (cottage number, e.g. "501")
 *   cottage → room.number
 */
export const rooms = ROOMS.map(r => ({
  ...r,
  name:    r.number,
  cottage: r.number,
}));

/**
 * Get room by number.
 */
export function getRoomByNumber(number) {
  return ROOMS.find(r => r.number === number) || null;
}

/**
 * Get all rooms in a zone.
 */
export function getRoomsByZone(zone) {
  return ROOMS.filter(r => r.zone === zone);
}

/**
 * Get zone for a room number.
 */
export function getZoneForRoom(roomNumber) {
  const room = getRoomByNumber(roomNumber);
  return room ? room.zone : null;
}
